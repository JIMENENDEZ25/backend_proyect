    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
    String idStr = txtIdLocalidad.getText().trim();

    if (idStr.isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "‚ö†Ô∏è Debe ingresar el ID de la localidad que desea eliminar.",
            "Validaci√≥n", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this,
        "¬øEst√°s seguro de eliminar esta localidad?",
        "Confirmar eliminaci√≥n",
        JOptionPane.YES_NO_OPTION,
        JOptionPane.WARNING_MESSAGE);

    if (confirm != JOptionPane.YES_OPTION) return;

    try {
        int id = Integer.parseInt(idStr);
        services.localidad_service service = new services.localidad_service();
        service.deleteLocalidad(id);

        JOptionPane.showMessageDialog(this,
            "üóëÔ∏è Localidad eliminada correctamente.",
            "√âxito", JOptionPane.INFORMATION_MESSAGE);

        txtIdLocalidad.setText("");
        txtNombreLocalidad.setText("");
        txtPrecioLocalidad.setText("");

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this,
            "‚ùå Error al eliminar la localidad:\n" + ex.getMessage(),
            "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnEliminarActionPerformed

